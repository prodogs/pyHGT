export class Profile {
  profileDB: ProfileDB = new ProfileDB();
  profileSlots: ProfileSlotsDB[] = [];

  constructor() {}

  static GetProfile(
    departmentName: string,
    itemName: string,
    featureName: string,
    featureSize: string
  ): Profile {

    let profile = new Profile();  
    let profilePart = ProfileDB.GetProfile(departmentName,itemName,featureName,featureSize);
  
    if (profilePart.profile_number>0) {
      profile.profileDB = profilePart;
      profile.profileSlots = ProfileSlotsDB.GetProfileRecords(profilePart.profile_number);
    }
    return profile;
  }
}
